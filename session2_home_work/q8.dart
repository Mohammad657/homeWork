//Q8- Write a Dart program that declares two numeric variables and performs addition, subtraction,
//multiplication, division, and modulo on them. Print each result.

void main (){
  num firstNumber = 8;
  num secondNumber = 4;

  print('firstNumber + secondNumber = ${firstNumber + secondNumber}');
  print('firstNumber - secondNumber = ${firstNumber - secondNumber}');
  print('firstNumber * secondNumber = ${firstNumber * secondNumber}');
  print('firstNumber / secondNumber = ${firstNumber / secondNumber}');
  print('firstNumber % secondNumber = ${firstNumber % secondNumber}');
}